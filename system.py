def ability_modifier(score):
    """Модификатор характеристики как в 5e: (значение - 10) // 2."""
    try:
        score = int(score)
    except (TypeError, ValueError):
        score = 10
    return (score - 10) // 2


def format_modifier(mod):
    """+3 / -1 / +0 — с явным знаком."""
    return f"+{mod}" if mod >= 0 else str(mod)


